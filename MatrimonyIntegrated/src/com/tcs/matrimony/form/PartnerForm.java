package com.tcs.matrimony.form;

import org.apache.struts.action.ActionForm;

public class PartnerForm extends ActionForm {
	
	private String pUserId;
	private String pMstatus;
	private String pMtongue;
	private String pReligion;
	private String pOccupation;
	private String pState;
	public String getpUserId() {
		return pUserId;
	}
	public void setpUserId(String pUserId) {
		this.pUserId = pUserId;
	}
	public String getpMstatus() {
		return pMstatus;
	}
	public void setpMstatus(String pMstatus) {
		this.pMstatus = pMstatus;
	}
	public String getpMtongue() {
		return pMtongue;
	}
	public void setpMtongue(String pMtongue) {
		this.pMtongue = pMtongue;
	}
	public String getpReligion() {
		return pReligion;
	}
	public void setpReligion(String pReligion) {
		this.pReligion = pReligion;
	}
	public String getpOccupation() {
		return pOccupation;
	}
	public void setpOccupation(String pOccupation) {
		this.pOccupation = pOccupation;
	}
	public String getpState() {
		return pState;
	}
	public void setpState(String pState) {
		this.pState = pState;
	}
	
	

}
